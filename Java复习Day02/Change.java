

public class Change{
	public static void main(String[] args){
		//演示制表符,实现对齐功能
		System.out.println("北京\t天津\t上海");
		//演示换行符
		System.out.println("Jack\nmary\nsmith");
		//输出\
		System.out.println("C:\\Windows\\System32\\cmd.exe");
		//输出\\ \\
		System.out.println("\\\\"+"====="+"\\\\");
		//输出 老师说:"要好好学习"
		System.out.println("老师说:"+"\""+"要好好学习\"");
		//演示\r
		System.out.println("你好\r李焕英");
	}
}